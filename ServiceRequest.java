    private void usernameFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_usernameFieldFocusLost
        // TODO add your handling code here:
        if(usernameField.getText().length() > 20)
        {
            usernameAlert.setVisible(true);
          
            username=false;
            setTextFieldBorderColor(usernameField, Color.RED);
        }
        else if(usernameField.getText().equals(""))
        {
             username=false;
             usernameAlert.setVisible(false);
             setTextFieldBorderColor(usernameField, Color.RED);
        }
        else
        {
            SystemLayer system = new SystemLayer();
            if(system.usernameAlreadyTaken(usernameField.getText())==true)
            {
                username=true;
                usernameAlert.setVisible(false);
                setTextFieldBorderColor(usernameField, Color.BLACK);
            }
            else{
                username=false;
                usernameAlert.setVisible(true);
                setTextFieldBorderColor(usernameField, Color.RED);
            }
        }
    }//GEN-LAST:event_usernameFieldFocusLost
      public boolean validateEmail(String email)
    {
        final String EMAIL_REGEX =
            "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";

        // Compile the regex pattern
        Pattern pattern = Pattern.compile(EMAIL_REGEX);

        // Match the input email against the pattern
        Matcher matcher = pattern.matcher(email);

        // Return true if the email matches the pattern, false otherwise
        return matcher.matches();
    
    }
