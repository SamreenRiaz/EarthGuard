    private void passwordFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_passwordFieldFocusLost
        // TODO add your handling code here:
        if(emailValid==true ||phoneValid==true)
        {
            String password = new String(passwordField.getPassword());
            if(password.length()> 15 && !password.equals(""))
            {
                maxLengthAlert.setVisible(true);
                passwordValid=false;
                setTextFieldBorderColor(passwordField, Color.RED);
            }
            else if(password.equals(""))
            {
                 passwordValid=false;
                 maxLengthAlert.setVisible(false);
                 setTextFieldBorderColor(passwordField, Color.RED);
            }
            else
            {
                passwordValid=true;
                maxLengthAlert.setVisible(false);
                setTextFieldBorderColor(passwordField, Color.black);
            }
        }
    }//GEN-LAST:event_passwordFieldFocusLost
