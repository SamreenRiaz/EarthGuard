    private void confirmPasswordFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_confirmPasswordFieldFocusLost
        // TODO add your handling code here:
        if(emailValid==true||phoneValid==true)
        {
            String cpassword = new String(confirmPasswordField.getPassword());
            String password = new String(passwordField.getPassword());
            if(passwordValid==true && !cpassword.equals(""))
            {
                if(!password.equals(cpassword))
                {
                    matchingAlert.setVisible(true);
                    setTextFieldBorderColor(confirmPasswordField, Color.RED);
                    cPassValid=false;
                }
                else {
                    matchingAlert.setVisible(false);
                    setTextFieldBorderColor(confirmPasswordField, Color.BLACK);
                    cPassValid=true;
                }   
            }
             if(password.equals("") && !cpassword.equals(""))
            {

                    matchingAlert.setVisible(true);
                    setTextFieldBorderColor(confirmPasswordField, Color.RED);
                    cPassValid=false;
            }
            if(cpassword.equals(""))
            {
                matchingAlert.setVisible(false);
                cPassValid=false;
                setTextFieldBorderColor(confirmPasswordField, Color.RED);
            }
            if(cpassword.equals("")&&!password.equals(""))
            {
                matchingAlert.setVisible(true);
                cPassValid=false;
                setTextFieldBorderColor(confirmPasswordField, Color.RED);
            }
        }
    }//GEN-LAST:event_confirmPasswordFieldFocusLost

